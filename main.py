##메서드
def add_func(n1,n2):
    res = n1 + n2
    return res

def sub_func(n1,n2):
    res = n1 - n2
    return res

def multiple_func(n1,n2):
    res = n1 * n2
    return res


##전역변수

num1, num2, result = 100, 200, 0






##메인코드
result = add_func(num1, num2)
print(num1,'+',num2,'=',result)

result = sub_func(num1, num2)
print(num1,'-',num2,'=',result)

result = multiple_func(num1, num2)
print(num1,'*',num2,'=',result)

